package beans;

public class UsuarioDTO {
	private String codUsuario, nomUsuario, apePatUsuario, apeMatUsuario, tipoUsuario, numDocUsuario, imgUsuario, fecNacUsuario;
	private int edadUsuario;
	private boolean adminUsuario;
	public String getCodUsuario() {
		return codUsuario;
	}
	public void setCodUsuario(String codUsuario) {
		this.codUsuario = codUsuario;
	}
	public String getNomUsuario() {
		return nomUsuario;
	}
	public void setNomUsuario(String nomUsuario) {
		this.nomUsuario = nomUsuario;
	}
	public String getApePatUsuario() {
		return apePatUsuario;
	}
	public void setApePatUsuario(String apePatUsuario) {
		this.apePatUsuario = apePatUsuario;
	}
	public String getApeMatUsuario() {
		return apeMatUsuario;
	}
	public void setApeMatUsuario(String apeMatUsuario) {
		this.apeMatUsuario = apeMatUsuario;
	}
	public String getTipoUsuario() {
		return tipoUsuario;
	}
	public void setTipoUsuario(String tipoUsuario) {
		this.tipoUsuario = tipoUsuario;
	}
	public String getNumDocUsuario() {
		return numDocUsuario;
	}
	public void setNumDocUsuario(String numDocUsuario) {
		this.numDocUsuario = numDocUsuario;
	}
	public String getImgUsuario() {
		return imgUsuario;
	}
	public void setImgUsuario(String imgUsuario) {
		this.imgUsuario = imgUsuario;
	}
	public String getFecNacUsuario() {
		return fecNacUsuario;
	}
	public void setFecNacUsuario(String fecNacUsuario) {
		this.fecNacUsuario = fecNacUsuario;
	}
	public int getEdadUsuario() {
		return edadUsuario;
	}
	public void setEdadUsuario(int edadUsuario) {
		this.edadUsuario = edadUsuario;
	}
	public boolean isAdminUsuario() {
		return adminUsuario;
	}
	public void setAdminUsuario(boolean adminUsuario) {
		this.adminUsuario = adminUsuario;
	}
	
}
